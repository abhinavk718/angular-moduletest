class Operators
{
       public static void main(String  []args)
       {
          int a=10;
          double d=a;
          System.out.println("a is "+a);
          System.out.println("d is "+d);
          double c=20.6;
          System.out.println("c is "+c);
          int x=10;
          double z=28.7;
          double y=x+z;
          System.out.println("y is "+y);
          int p=5/2;
          System.out.println("p is "+p);
          int j=7;
          double k=2.0;
          double l=j/k;
          System.out.println("l is "+l);
       }
}